package javafx.print;

/**
 * Class which describes whether printing should be monochrome
 * or color.
 * @since JavaFX 8.0
 */
public enum PrintColor {

    /**
     * Specifies color printing.
     */
    COLOR,

    /**
     * Specifies monochrome or grey scale printing.
     */
    MONOCHROME
}
